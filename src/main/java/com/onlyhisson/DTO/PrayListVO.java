package com.onlyhisson.DTO;

public class PrayListVO {
	
	private String email;
	private String date;
	private String pray_list;
	
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getPray_list() {
		return pray_list;
	}
	public void setPray_list(String pray_list) {
		this.pray_list = pray_list;
	}
}
